"""
Answer quality metrics — faithfulness evaluation using LLM-as-judge.

Why LLM-as-judge?
Answer quality can't be measured with deterministic assertions — there's no
single "correct" wording of a good answer. Instead, we ask Claude to evaluate
whether an answer is faithful to the retrieved context: does every claim in
the answer appear in the context, or does the answer introduce information
that wasn't retrieved?

This is sometimes called "groundedness" or "contextual faithfulness" in RAG
evaluation literature (RAGAS, TruLens, and similar frameworks use this approach).

The judge is asked to:
1. Read the context (retrieved chunks)
2. Read the answer (generated by the agent)
3. Identify any claims in the answer NOT supported by the context
4. Return a score 0.0–1.0 and a list of unsupported claims
"""

from __future__ import annotations

import json
import re
from dataclasses import dataclass, field

from src.llm.client import ClaudeClient


@dataclass
class FaithfulnessResult:
    """
    Result of a faithfulness evaluation.

    score             : 0.0 (fully hallucinated) to 1.0 (fully grounded)
    unsupported_claims: specific claims in the answer not found in context
    is_faithful       : True if score >= faithfulness_threshold (default 0.8)
    raw_judge_output  : the judge's raw response (for debugging)
    """
    score: float
    unsupported_claims: list[str] = field(default_factory=list)
    is_faithful: bool = True
    raw_judge_output: str = ""


_FAITHFULNESS_PROMPT = """\
You are evaluating whether an AI assistant's answer is faithful to the provided context.

FAITHFUL means: every factual claim in the answer is directly supported by \
information in the context. The answer may paraphrase or summarise the context, \
but must not introduce new facts, numbers, or conclusions not present in the context.

Context (retrieved documents):
{context}

Answer to evaluate:
{answer}

Instructions:
1. Identify every factual claim in the answer (numbers, percentages, names, events, conclusions).
2. For each claim, check whether it is supported by the context.
3. List any claims that are NOT supported by the context.

Respond with ONLY valid JSON in this exact format — no markdown, no explanation:
{{"score": <float 0.0-1.0>, "unsupported_claims": [<list of unsupported claim strings>]}}

score = 1.0 means all claims are supported.
score = 0.0 means no claims are supported.
score = 0.5 means roughly half the claims are supported.
If there are no factual claims to evaluate, return {{"score": 1.0, "unsupported_claims": []}}.
"""


async def evaluate_faithfulness(
    answer: str,
    context: str,
    judge_client: ClaudeClient,
    faithfulness_threshold: float = 0.8,
) -> FaithfulnessResult:
    """
    Asks the LLM judge to evaluate whether the answer is grounded in the context.

    Parameters:
        answer               : the agent's generated answer
        context              : the retrieved chunks concatenated as text
        judge_client         : a ClaudeClient instance (can be mocked in tests)
        faithfulness_threshold: score below which is_faithful = False

    Returns a FaithfulnessResult with a 0-1 score and list of unsupported claims.
    """
    prompt = _FAITHFULNESS_PROMPT.format(context=context, answer=answer)

    response = await judge_client.complete(
        messages=[{"role": "user", "content": prompt}],
        system="You are a precise evaluator. Respond only with valid JSON.",
    )

    raw_output = ""
    for block in response.content:
        if getattr(block, "type", None) == "text":
            raw_output = block.text
            break

    score, unsupported = _parse_judge_response(raw_output)

    return FaithfulnessResult(
        score=score,
        unsupported_claims=unsupported,
        is_faithful=score >= faithfulness_threshold,
        raw_judge_output=raw_output,
    )


def _parse_judge_response(raw: str) -> tuple[float, list[str]]:
    """
    Parses the judge's JSON response.

    Falls back gracefully on malformed output — returns (0.0, ["parse error"])
    rather than crashing the evaluation run.
    """
    try:
        # Strip any accidental markdown code fences
        cleaned = re.sub(r"```(?:json)?|```", "", raw).strip()
        data = json.loads(cleaned)
        score = float(data.get("score", 0.0))
        score = max(0.0, min(1.0, score))  # clamp to [0, 1]
        unsupported = [str(c) for c in data.get("unsupported_claims", [])]
        return score, unsupported
    except (json.JSONDecodeError, KeyError, TypeError, ValueError):
        return 0.0, [f"Failed to parse judge response: {raw[:200]}"]
